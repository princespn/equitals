<template>
	<!-- Start content -->
	<div class="content">
		<div class="">
		    <div class="page-header-title">
		        <h4 class="page-title">Manage Videos</h4>
		    </div>
		</div>

		<div class="page-content-wrapper">
		    <div class="container">
		        <div class="row">
                    <div class="col-md-12 col-sm-3">
                        <div class="panel panel-primary">
                            <div class="panel-body add-new">
                                <router-link class="btn btn-primary waves-effect waves-light" :to="{name: 'addvideo'}">
                                    <i class="fa fa-plus"></i>Add Video
                                </router-link>
                            </div>
                        </div>
                    </div>
                </div>
		        <div class="row">
		            <div class="col-md-12">
		                <div class="panel panel-primary">
		                    <div class="panel-body">
		                        <table id="responsive-datatable" class="table table-striped table-bordered dt-responsive">
		                            <thead>
		                                <tr>
		                                    <th>Sr.No</th>
		                                    <th>Video</th>
		                                    <th>Description</th>
		                                    <th>Link</th>
		                                    <th>Date</th>
		                                    <th>Action</th>
		                                </tr>
		                            </thead>
		                
		                            <tfoot>
		                                <tr>
		                                    <th>Sr.No</th>
		                                    <th>Video</th>
		                                    <th>Description</th>
		                                    <th>Link</th>
		                                    <th>Date</th>
		                                    <th>Action</th>
		                                </tr>
		                            </tfoot>
		                        </table>
		                    </div>
		                </div>
		            </div>
		        </div><!-- end row -->
		    </div><!-- container -->
		</div><!-- Page content Wrapper -->
	</div><!-- content -->
</template>

<script>
    import { apiAdminHost } from'./../../admin-config/config';
    import moment from 'moment';
    import Swal from 'sweetalert2';

    export default {
    	
        data() {
            return {
                user_data  : [],
                products   : [],
                length : 10,
                start  : 0,
            }
        },
        mounted() {
        	
            this.manageWhatsNew();
        },
        methods: {
            manageWhatsNew(){
            	let i = 0;
                let that = this;
                let token = localStorage.getItem('access_token');
                setTimeout(function(){
                    that.table = $('#responsive-datatable').DataTable({
                        retrieve: true,
                        destroy: true,
                        processing: false,
                        serverSide: true,
                        responsive: true,
                        stateSave: false,
                        ordering: false,
                        dom: 'Bfrtip',
                        lengthMenu: [[10, 50, 100], [10, 50, 100]],
                        buttons: [
                            'pageLength',
                            //'colvis',
                            {
                                extend: 'excelHtml5',
                                title: 'Videos Report',
                                exportOptions: {
                                    columns: ':visible'
                                }
                            },
                            {
                                extend: 'csvHtml5',
                                title: 'Videos Report',
                                exportOptions: {
                                    columns: ':visible'
                                }
                            },
                            {
                                extend: 'pdfHtml5',
                                title: 'Videos Report',
                                exportOptions: {
                                    columns: ':visible'
                                }
                            },
                            /*{
                                extend: 'print',
                                title: 'Videos Report',
                                exportOptions: {
                                    columns: ':visible'
                                }
                            },*/
                        ],
                        ajax: {
                            url: apiAdminHost+'/show/videos',
                            type: 'POST',
                            data: function (d) {
                                i = 0;
                                i = d.start + 1;

                                let params = {
                                    provide : 1
                                };
                                Object.assign(d, params);
                                return d;
                            },
                            headers: {
                              'Authorization': 'Bearer ' + token
                            },
                            dataSrc: function (json) {
                                if (json.code === 200) {
                                    that.arrGetHelp = json.data.records;
                                    json['draw'] = json.data.draw;
                                    json['recordsFiltered'] = json.data.recordsFiltered;
                                    json['recordsTotal'] = json.data.recordsTotal;
                                    return json.data.records;
                                } else {
                                    json['draw'] = 0;
                                    json['recordsFiltered'] = 0;
                                    json['recordsTotal'] = 0;
                                    return json;
                                }
                            }
                        },
                        columns: [
                            {
                                render: function (data, type, row, meta) {
                                    return meta.row + meta.settings._iDisplayStart + 1;
                                }
                            },
                            { data: 'name' },
                            { data: 'text' },
                            {
                              	render: function (data, type, row, meta) {
                                	return `<iframe width="250" height="200" src="${row.link}"></iframe>`;
                              	}
                            },
                            {
                                render: function (data, type, row, meta) {
                                    if (row.created_at === null || row.created_at === undefined || row.created_at === '') {
                                      return `-`;
                                    } else {
                                        return moment(String(row.created_at)).format('YYYY-MM-DD');
                                    }
                                }
                            },
                            {
                              	render: function (data, type, row, meta) {
                                	return `<a id="onUpdateClick" data-id="${row.id}">
                                                <i class="fa fa-pencil font-16"></i>
                                            </a>&nbsp;
                                			<a id="onDeleteClick" data-id="${row.id}">
                                                <i class="fa fa-trash text-danger font-16"></i>
                                            </a>`;
                              	}
                            }
                        ]
                    });

                    $('#responsive-datatable').on('click', '#onUpdateClick',function () {
                        that.$router.push({
                            name:'addvideo',
                            params:{
                                id: $(this).data('id'),
                            }
                        });
                    });

                    $('#responsive-datatable').on('click', '#onDeleteClick',function () {
                        that.deleteVideo($(this).data('id'));
                    });

                },0);
            },
            deleteVideo(id){
                Swal({
                    title: 'Are you sure ?',
                    text: "You won't be delete this!",
                    type: 'warning',
                    showCancelButton: true,
                    confirmButtonText: 'Yes!',
                    cancelButtonText: 'No'
                }).then((result) => {
                    if (result.value) {
                        axios.post('/delete/video',{
                            id: id
                        }).then(resp => {
                            if(resp.data.code === 200){
                                this.$toaster.success(resp.data.message);
                                this.table.ajax.reload();
                            } else {
                                this.$toaster.error(resp.data.message);
                            }
                        }).catch(err => {
                            console.log(err);
                        });
                    }
                });
            }
        }
    }
</script>	